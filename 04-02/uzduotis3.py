"""
Sukurkite mini python programą, kuri kaip įvestį
paimtų du skaičius ir grąžintų jų sumą, atimtį, dalybą, daugybą.
"""


def skaiciavimai(skaicius1: int, skaicius2: int):
    print("Skaiciu sudetis:", skaicius1 + skaicius2)
    print("Skaiciu atimtis:", skaicius1 - skaicius2)
    print("Skaiciu dalyba: ", skaicius1 / skaicius2)
    print("skaiciu daugyba:", skaicius1 * skaicius2)


skaiciavimai(5, 8)
